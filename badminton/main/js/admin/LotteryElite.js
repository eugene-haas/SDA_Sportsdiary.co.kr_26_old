/* ==================================================================================
      seed no  seed text . 1, 2, 3/4, 5/8, 9/16  
   ================================================================================== */
function getStrSeed(nSeed) {
    var strSeed = nSeed;
    nSeed = Number(nSeed);

    if (nSeed > 2 && nSeed < 5) strSeed = "3/4";
    else if (nSeed > 4 && nSeed < 9) strSeed = "5/8";
    else if (nSeed > 8 && nSeed < 17) strSeed = "9/16";

    return strSeed;
}
